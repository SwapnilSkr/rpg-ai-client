/// The two orbital-plane rings the discs ride on, rendered as 3D forged-metal
/// bands. Each ring is a tilted ellipse drawn as one half at a time (so the FAR
/// half can sit behind the sigil and the NEAR half in front). The band has a
/// neumorphic bevel — a light top-left highlight edge and a dark bottom-right
/// shadow edge — plus a skeuomorphic specular glint that travels around the rim.
class _OrbitRingsPainter extends CustomPainter {
  final double glint; // 0..1 traveling-light phase
  final double reveal; // 0..1 fade + grow-in
  final bool front; // near (bottom) half vs far (top) half
  _OrbitRingsPainter({
    required this.glint,
    required this.reveal,
    required this.front,
  });

  @override
  void paint(Canvas canvas, Size size) {
    if (reveal <= 0.01) return;
    final c = size.center(Offset.zero);
    final r = Curves.easeOutCubic.transform(reveal.clamp(0.0, 1.0));
    _ring(canvas, c, _kOuterRx * r, _kOuterRx * _kOuterTilt * r, glint, r);
    // Inner ring's glint is phase-offset so the two shines don't move in lockstep.
    _ring(
      canvas,
      c,
      _kInnerRx * r,
      _kInnerRx * _kInnerTilt * r,
      (glint + 0.5) % 1.0,
      r,
    );
  }

  void _ring(
    Canvas canvas,
    Offset c,
    double rx,
    double ry,
    double glintPhase,
    double a,
  ) {
    if (rx < 1) return;
    final start = front ? 0.0 : math.pi; // bottom half (sin>0) vs top half
    const sweep = math.pi;
    final rect = Rect.fromCenter(center: c, width: rx * 2, height: ry * 2);

    // Soft energy halo (neumorphic softness + anime glow).
    canvas.drawArc(
      rect,
      start,
      sweep,
      false,
      Paint()
        ..style = PaintingStyle.stroke
        ..strokeWidth = 9
        ..color = EverloreTheme.gold.withValues(alpha: 0.05 * a)
        ..maskFilter = const MaskFilter.blur(BlurStyle.normal, 6),
    );

    // Forged-metal band base.
    canvas.drawArc(
      rect,
      start,
      sweep,
      false,
      Paint()
        ..style = PaintingStyle.stroke
        ..strokeWidth = 2.6
        ..strokeCap = StrokeCap.round
        ..color = EverloreTheme.goldDim.withValues(alpha: 0.6 * a),
    );

    // Neumorphic top-left highlight bevel.
    canvas.save();
    canvas.translate(-0.8, -1.2);
    canvas.drawArc(
      rect,
      start,
      sweep,
      false,
      Paint()
        ..style = PaintingStyle.stroke
        ..strokeWidth = 1.1
        ..color = EverloreTheme.goldGlow.withValues(alpha: 0.5 * a),
    );
    canvas.restore();

    // Neumorphic bottom-right shadow bevel.
    canvas.save();
    canvas.translate(0.8, 1.4);
    canvas.drawArc(
      rect,
      start,
      sweep,
      false,
      Paint()
        ..style = PaintingStyle.stroke
        ..strokeWidth = 1.3
        ..color = Colors.black.withValues(alpha: 0.5 * a),
    );
    canvas.restore();

    // Skeuomorphic specular glint travelling around the rim — drawn only while
    // its centre is on the half this pass renders, so it slides behind/in front
    // of the sigil naturally as it orbits.
    final ga = glintPhase * 2 * math.pi;
    final s = math.sin(ga);
    final onThisHalf = front ? s >= 0 : s < 0;
    if (onThisHalf) {
      const half = 0.45; // arc half-width (radians)
      // fade near the half boundary so it doesn't pop when crossing the sigil
      final edgeFade = (s.abs() / 0.35).clamp(0.0, 1.0);
      canvas.drawArc(
        rect,
        ga - half,
        half * 2,
        false,
        Paint()
          ..style = PaintingStyle.stroke
          ..strokeWidth = 3.0
          ..strokeCap = StrokeCap.round
          ..color = EverloreTheme.goldGlow.withValues(alpha: 0.9 * a * edgeFade)
          ..maskFilter = const MaskFilter.blur(BlurStyle.normal, 2.5),
      );
    }
  }

  @override
  bool shouldRepaint(_OrbitRingsPainter old) =>
      old.glint != glint || old.reveal != reveal || old.front != front;
}

/// Background vignette + a field of slow-drifting ember motes.
class _AmbientPainter extends CustomPainter {
  final double t;
  _AmbientPainter(this.t);

  static final List<_Ember> _embers = List.generate(18, (i) {
    final rnd = math.Random(i * 7 + 3);
    return _Ember(
      x: rnd.nextDouble(),
      baseY: rnd.nextDouble(),
      speed: 0.3 + rnd.nextDouble() * 0.7,
      size: 0.8 + rnd.nextDouble() * 1.8,
      phase: rnd.nextDouble(),
      gold: rnd.nextBool(),
    );
  });

  @override
  void paint(Canvas canvas, Size size) {
    canvas.drawRect(
      Offset.zero & size,
      Paint()
        ..shader = RadialGradient(
          center: const Alignment(0, -0.12),
          radius: 0.9,
          colors: [
            EverloreTheme.void2.withValues(alpha: 0.55),
            EverloreTheme.void0,
          ],
        ).createShader(Offset.zero & size),
    );

    for (final e in _embers) {
      final prog = (t * e.speed + e.phase) % 1.0;
      final y = (e.baseY - prog) % 1.0;
      final twinkle =
          0.3 + 0.7 * (0.5 + 0.5 * math.sin((prog + e.phase) * math.pi * 4));
      final dx =
          e.x * size.width + math.sin((prog + e.phase) * math.pi * 2) * 10;
      final dy = y * size.height;
      final color = e.gold
          ? EverloreTheme.goldGlow
          : EverloreTheme.violetBright;
      canvas.drawCircle(
        Offset(dx, dy),
        e.size,
        Paint()
          ..color = color.withValues(alpha: 0.10 + twinkle * 0.22)
          ..maskFilter = const MaskFilter.blur(BlurStyle.normal, 1.5),
      );
    }
  }

  @override
  bool shouldRepaint(_AmbientPainter old) => old.t != t;
}

class _Ember {
  final double x, baseY, speed, size, phase;
  final bool gold;
  const _Ember({
    required this.x,
    required this.baseY,
    required this.speed,
    required this.size,
    required this.phase,
    required this.gold,
  });
}

/// "EVERLORE" engraving in letter by letter, each with a letterpress depth
/// (dark drop behind a gold-gradient face).
class _EngravedWordmark extends StatelessWidget {
  final double reveal; // 0..1
  const _EngravedWordmark({required this.reveal});

  static const _letters = ['E', 'V', 'E', 'R', 'L', 'O', 'R', 'E'];

  @override
  Widget build(BuildContext context) {
    final base = GoogleFonts.cinzel(
      fontSize: 34,
      fontWeight: FontWeight.w700,
      letterSpacing: 8.0,
    );
    return ShaderMask(
      shaderCallback: (rect) => const LinearGradient(
        begin: Alignment.topCenter,
        end: Alignment.bottomCenter,
        colors: [
          EverloreTheme.goldGlow,
          EverloreTheme.gold,
          EverloreTheme.goldDim,
        ],
        stops: [0.0, 0.5, 1.0],
      ).createShader(rect),
      child: Row(
        mainAxisSize: MainAxisSize.min,
        children: List.generate(_letters.length, (i) {
          // Each letter reveals across its own slice of the timeline.
          final start = i / _letters.length * 0.7;
          final p = ((reveal - start) / 0.3).clamp(0.0, 1.0);
          final eased = Curves.easeOut.transform(p);
          return Opacity(
            opacity: eased,
            child: Transform.translate(
              offset: Offset(0, (1 - eased) * 6),
              child: Stack(
                children: [
                  Transform.translate(
                    offset: const Offset(0, 1.6),
                    child: Text(
                      _letters[i],
                      style: base.copyWith(
                        color: Colors.black.withValues(alpha: 0.6),
                      ),
                    ),
                  ),
                  Text(_letters[i], style: base.copyWith(color: Colors.white)),
                ],
              ),
            ),
          );
        }),
      ),
    );
  }
}
